# PERFORMANCE ANALYTICS
# ============================================================

class PerformanceAnalytics:
    """
    Computes performance metrics from backtest results.
    
    Metrics:
    - Sharpe Ratio (annualized, risk-free = 0)
    - Sortino Ratio (downside deviation only)
    - Max Drawdown and duration
    - Win Rate, Profit Factor
    - Average Win/Loss
    - Confidence calibration
    """
    
    @staticmethod
    def compute(
        trades: List[TradeRecord],
        equity_curve: List[PortfolioSnapshot],
        initial_capital: float,
        risk_free_rate: float = 0.0
    ) -> Dict:
        """Compute all performance metrics."""
        if not trades:
            return {
                "total_trades": 0,
                "message": "No trades executed"
            }
        
        # Basic stats
        pnls = [t.pnl for t in trades]
        pnl_pcts = [t.pnl_pct for t in trades]
        winners = [p for p in pnls if p > 0]
        losers = [p for p in pnls if p < 0]
        
        total_trades = len(trades)
        winning_trades = len(winners)
        losing_trades = len(losers)
        win_rate = winning_trades / total_trades if total_trades > 0 else 0.0
        
        # P&L
        total_pnl = sum(pnls)
        total_return = total_pnl / initial_capital
        
        avg_win = statistics.mean(winners) if winners else 0.0
        avg_loss = statistics.mean(losers) if losers else 0.0
        
        gross_profit = sum(winners) if winners else 0.0
        gross_loss = abs(sum(losers)) if losers else 1.0
        profit_factor = gross_profit / gross_loss if gross_loss > 0 else float('inf')
        
        # Holding period
        holding_periods = [t.holding_period for t in trades]
        avg_holding = sum(holding_periods, timedelta()) / len(holding_periods)
        
        # Annualized return
        if len(equity_curve) >= 2:
            total_days = (equity_curve[-1].timestamp - equity_curve[0].timestamp).days
            total_days = max(total_days, 1)
            annualized_return = (1 + total_return) ** (365 / total_days) - 1
        else:
            annualized_return = total_return
        
        # Sharpe Ratio (annualized)
        if len(pnl_pcts) > 1:
            mean_ret = statistics.mean(pnl_pcts)
            std_ret = statistics.stdev(pnl_pcts)
            # Assume ~252 trading events per year
            sharpe = (mean_ret - risk_free_rate / 252) / std_ret * math.sqrt(252) if std_ret > 0 else 0.0
        else:
            sharpe = 0.0
        
        # Sortino Ratio (downside deviation only)
        downside_returns = [r for r in pnl_pcts if r < 0]
        if len(downside_returns) > 1:
            downside_dev = statistics.stdev(downside_returns)
            mean_ret = statistics.mean(pnl_pcts)
            sortino = (mean_ret - risk_free_rate / 252) / downside_dev * math.sqrt(252) if downside_dev > 0 else 0.0
        else:
            sortino = sharpe  # No downside → same as Sharpe
        
        # Max drawdown from equity curve
        max_dd = 0.0
        peak = initial_capital
        max_dd_duration = timedelta(0)
        dd_start = None
        
        for snap in equity_curve:
            if snap.equity > peak:
                peak = snap.equity
                dd_start = None
            else:
                dd = (peak - snap.equity) / peak
                if dd > max_dd:
                    max_dd = dd
                if dd_start is None:
                    dd_start = snap.timestamp
                elif snap.timestamp - dd_start > max_dd_duration:
                    max_dd_duration = snap.timestamp - dd_start
        
        # Directional accuracy
        direction_correct = sum(
            1 for t in trades
            if (t.direction == "bullish" and t.pnl > 0) or
               (t.direction == "bearish" and t.pnl > 0)
        )
        directional_accuracy = direction_correct / total_trades
        
        # Confidence calibration: do high-confidence trades win more?
        if total_trades >= 5:
            high_conf = [t for t in trades if t.signal_confidence >= 0.6]
            low_conf = [t for t in trades if t.signal_confidence < 0.6]
            high_wr = sum(1 for t in high_conf if t.pnl > 0) / max(len(high_conf), 1)
            low_wr = sum(1 for t in low_conf if t.pnl > 0) / max(len(low_conf), 1)
            calibration = high_wr - low_wr  # Positive = well calibrated
        else:
            calibration = 0.0
        
        avg_confidence = statistics.mean([t.signal_confidence for t in trades])
        
        final_capital = equity_curve[-1].equity if equity_curve else initial_capital + total_pnl
        
        return {
            "start_date": trades[0].entry_time,
            "end_date": trades[-1].exit_time,
            "initial_capital": initial_capital,
            "final_capital": round(final_capital, 2),
            "total_trades": total_trades,
            "winning_trades": winning_trades,
            "losing_trades": losing_trades,
            "total_return": round(total_return, 6),
            "annualized_return": round(annualized_return, 6),
            "sharpe_ratio": round(sharpe, 4),
            "sortino_ratio": round(sortino, 4),
            "max_drawdown": round(max_dd, 6),
            "max_drawdown_duration": max_dd_duration,
            "win_rate": round(win_rate, 4),
            "profit_factor": round(profit_factor, 4),
            "avg_win": round(avg_win, 2),
            "avg_loss": round(avg_loss, 2),
            "avg_holding_period": avg_holding,
            "directional_accuracy": round(directional_accuracy, 4),
            "avg_signal_confidence": round(avg_confidence, 4),
            "confidence_calibration": round(calibration, 4),
        }


