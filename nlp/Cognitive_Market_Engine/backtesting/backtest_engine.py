class PositionTracker:
    """
    Tracks simulated portfolio during backtesting.
    
    Supports:
    - Long and short positions
    - Stop loss and take profit
    - Position sizing limits
    - Equity curve tracking
    """
    
    def __init__(self, initial_capital: float = 100_000.0, max_position_pct: float = 0.10):
        self.initial_capital = initial_capital
        self.capital = initial_capital
        self.max_position_pct = max_position_pct
        
        self.open_positions: List[Dict] = []
        self.closed_trades: List[TradeRecord] = []
        self.equity_curve: List[PortfolioSnapshot] = []
        
        self._peak_equity = initial_capital
        self._max_drawdown = 0.0
        self._drawdown_start: Optional[datetime] = None
        self._max_drawdown_duration = timedelta(0)
    
    def open_position(
        self,
        timestamp: datetime,
        direction: str,
        price: float,
        size_pct: float,
        stop_loss: Optional[float],
        take_profit: Optional[float],
        event_id: str,
        confidence: float
    ) -> bool:
        """Open a new position. Returns True if successfully opened."""
        # Enforce max position size
        actual_size = min(size_pct, self.max_position_pct)
        position_value = self.capital * actual_size
        
        if position_value <= 0 or self.capital <= 0:
            return False
        
        self.open_positions.append({
            "entry_time": timestamp,
            "direction": direction,
            "entry_price": price,
            "size_pct": actual_size,
            "position_value": position_value,
            "stop_loss": stop_loss,
            "take_profit": take_profit,
            "event_id": event_id,
            "confidence": confidence,
        })
        return True
    
    def check_exits(self, timestamp: datetime, current_price: float, max_holding: timedelta = timedelta(hours=24)):
        """Check all open positions for stop/target/timeout exits."""
        still_open = []
        
        for pos in self.open_positions:
            exit_reason = None
            exit_price = current_price
            
            # Check timeout
            if timestamp - pos["entry_time"] >= max_holding:
                exit_reason = "timeout"
            
            # Check stop loss
            if pos["stop_loss"] is not None:
                if pos["direction"] == "bullish" and current_price <= pos["stop_loss"]:
                    exit_reason = "stop_loss"
                    exit_price = pos["stop_loss"]
                elif pos["direction"] == "bearish" and current_price >= pos["stop_loss"]:
                    exit_reason = "stop_loss"
                    exit_price = pos["stop_loss"]
            
            # Check take profit
            if pos["take_profit"] is not None:
                if pos["direction"] == "bullish" and current_price >= pos["take_profit"]:
                    exit_reason = "take_profit"
                    exit_price = pos["take_profit"]
                elif pos["direction"] == "bearish" and current_price <= pos["take_profit"]:
                    exit_reason = "take_profit"
                    exit_price = pos["take_profit"]
            
            if exit_reason:
                self._close_position(pos, timestamp, exit_price, exit_reason)
            else:
                still_open.append(pos)
        
        self.open_positions = still_open
    
    def _close_position(self, pos: Dict, timestamp: datetime, exit_price: float, reason: str):
        """Close a position and record the trade."""
        if pos["direction"] == "bullish":
            pnl_pct = (exit_price - pos["entry_price"]) / pos["entry_price"]
        else:
            pnl_pct = (pos["entry_price"] - exit_price) / pos["entry_price"]
        
        pnl = pos["position_value"] * pnl_pct
        self.capital += pnl
        
        trade = TradeRecord(
            entry_time=pos["entry_time"],
            exit_time=timestamp,
            direction=pos["direction"],
            entry_price=pos["entry_price"],
            exit_price=exit_price,
            position_size=pos["size_pct"],
            pnl=round(pnl, 2),
            pnl_pct=round(pnl_pct, 6),
            holding_period=timestamp - pos["entry_time"],
            signal_confidence=pos["confidence"],
            event_id=pos["event_id"],
            exit_reason=reason,
        )
        self.closed_trades.append(trade)
    
    def snapshot(self, timestamp: datetime, current_price: float) -> PortfolioSnapshot:
        """Take a portfolio snapshot."""
        unrealized = 0.0
        for pos in self.open_positions:
            if pos["direction"] == "bullish":
                unrealized += pos["position_value"] * (
                    (current_price - pos["entry_price"]) / pos["entry_price"]
                )
            else:
                unrealized += pos["position_value"] * (
                    (pos["entry_price"] - current_price) / pos["entry_price"]
                )
        
        equity = self.capital + unrealized
        realized = sum(t.pnl for t in self.closed_trades)
        
        # Track drawdown
        if equity > self._peak_equity:
            self._peak_equity = equity
            self._drawdown_start = None
        else:
            dd = (self._peak_equity - equity) / self._peak_equity
            if dd > self._max_drawdown:
                self._max_drawdown = dd
            if self._drawdown_start is None:
                self._drawdown_start = timestamp
            else:
                dd_duration = timestamp - self._drawdown_start
                if dd_duration > self._max_drawdown_duration:
                    self._max_drawdown_duration = dd_duration
        
        snap = PortfolioSnapshot(
            timestamp=timestamp,
            capital=round(self.capital, 2),
            unrealized_pnl=round(unrealized, 2),
            realized_pnl=round(realized, 2),
            num_open_positions=len(self.open_positions),
            total_trades=len(self.closed_trades),
            equity=round(equity, 2),
        )
        self.equity_curve.append(snap)
        return snap


