class EventReplayEngine:
    """
    Replays historical events through the cognitive engine.
    
    Supports:
    - Loading events from JSON files
    - Sequential chronological replay
    - Injecting custom signal generators
    """
    
    def __init__(self):
        self.events: List[HistoricalEvent] = []
    
    def load_events_from_json(self, filepath: str) -> int:
        """Load historical events from a JSON file. Returns count loaded."""
        if not os.path.exists(filepath):
            logger.warning(f"Event file not found: {filepath}")
            return 0
        
        with open(filepath, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        for item in data:
            try:
                event = HistoricalEvent(
                    event_id=item.get("event_id", f"evt_{len(self.events)}"),
                    timestamp=datetime.fromisoformat(item["timestamp"]),
                    headline=item["headline"],
                    content=item.get("content", item["headline"]),
                    source=item.get("source", "unknown"),
                    event_type=item.get("event_type", "generic"),
                    price_at_event=float(item["price_at_event"]),
                    price_1h_after=float(item.get("price_1h_after", item["price_at_event"])),
                    price_4h_after=float(item.get("price_4h_after", item["price_at_event"])),
                    price_24h_after=float(item.get("price_24h_after", item["price_at_event"])),
                    vol_before=float(item.get("vol_before", 0.0)),
                    vol_after=float(item.get("vol_after", 0.0)),
                    metadata=item.get("metadata", {}),
                )
                self.events.append(event)
            except (KeyError, ValueError) as e:
                logger.warning(f"Skipping malformed event: {e}")
        
        # Sort chronologically
        self.events.sort(key=lambda e: e.timestamp)
        logger.info(f"Loaded {len(self.events)} historical events")
        return len(self.events)
    
    def add_event(self, event: HistoricalEvent):
        """Add a single event and maintain chronological order."""
        self.events.append(event)
        self.events.sort(key=lambda e: e.timestamp)
    
    def get_events_in_range(
        self,
        start: datetime,
        end: datetime
    ) -> List[HistoricalEvent]:
        """Get events within a date range."""
        return [e for e in self.events if start <= e.timestamp <= end]
    
    def replay(
        self,
        signal_generator: Callable[[HistoricalEvent], Optional[SignalRecord]],
        start: Optional[datetime] = None,
        end: Optional[datetime] = None
    ) -> List[Tuple[HistoricalEvent, Optional[SignalRecord]]]:
        """
        Replay events through a signal generator function.
        
        Args:
            signal_generator: function(HistoricalEvent) -> Optional[SignalRecord]
            start: optional start date filter
            end: optional end date filter
            
        Returns:
            List of (event, signal) pairs
        """
        events = self.events
        if start:
            events = [e for e in events if e.timestamp >= start]
        if end:
            events = [e for e in events if e.timestamp <= end]
        
        results = []
        for event in events:
            try:
                signal = signal_generator(event)
                results.append((event, signal))
            except Exception as e:
                logger.error(f"Signal generation failed for {event.event_id}: {e}")
                results.append((event, None))
        
        return results


