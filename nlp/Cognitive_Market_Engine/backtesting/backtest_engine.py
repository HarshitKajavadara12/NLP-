"""
BACKTESTING FRAMEWORK — Cognitive Market Engine

Purpose:
Historical event replay engine for measuring theoretical P&L,
signal accuracy, and model performance over time.

This is NOT a production trading system — it's a research tool for
validating whether the cognitive engine's predictions would have
been profitable historically.

Components:
1. EventReplayEngine — replays historical news + price data
2. PositionTracker — tracks simulated portfolio
3. PerformanceAnalytics — Sharpe, Sortino, drawdown, win rate
4. BacktestRunner — orchestrates end-to-end backtests
"""

from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple, Callable
from datetime import datetime, timedelta
from enum import Enum
import statistics
import math
import json
import os
import logging

logger = logging.getLogger("cme.backtesting")


# ============================================================
# DATA STRUCTURES
# ============================================================

class TradeAction(str, Enum):
    BUY = "buy"
    SELL = "sell"
    HOLD = "hold"
    CLOSE = "close"


@dataclass
class HistoricalEvent:
    """A single historical news event with associated market data."""
    event_id: str
    timestamp: datetime
    headline: str
    content: str
    source: str
    event_type: str  # rate_decision, earnings, geopolitical, etc.
    
    # Market data at the time of the event
    price_at_event: float
    price_1h_after: float
    price_4h_after: float
    price_24h_after: float
    
    # Realized volatility around event
    vol_before: float = 0.0
    vol_after: float = 0.0
    
    # Optional metadata
    metadata: Dict = field(default_factory=dict)


@dataclass
class SignalRecord:
    """A signal generated by the engine during backtesting."""
    timestamp: datetime
    event_id: str
    action: TradeAction
    direction: str  # "bullish", "bearish", "neutral"
    confidence: float
    position_size: float  # fraction of capital
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    reasoning: str = ""


@dataclass
class TradeRecord:
    """A completed trade (entry + exit)."""
    entry_time: datetime
    exit_time: datetime
    direction: str
    entry_price: float
    exit_price: float
    position_size: float
    pnl: float
    pnl_pct: float
    holding_period: timedelta
    signal_confidence: float
    event_id: str
    exit_reason: str  # "take_profit", "stop_loss", "timeout", "signal_reversal"


@dataclass
class PortfolioSnapshot:
    """Portfolio state at a point in time."""
    timestamp: datetime
    capital: float
    unrealized_pnl: float
    realized_pnl: float
    num_open_positions: int
    total_trades: int
    equity: float  # capital + unrealized


@dataclass
class BacktestResult:
    """Complete backtest output."""
    start_date: datetime
    end_date: datetime
    initial_capital: float
    final_capital: float
    total_trades: int
    winning_trades: int
    losing_trades: int
    
    # Performance metrics
    total_return: float
    annualized_return: float
    sharpe_ratio: float
    sortino_ratio: float
    max_drawdown: float
    max_drawdown_duration: timedelta
    win_rate: float
    profit_factor: float
    avg_win: float
    avg_loss: float
    avg_holding_period: timedelta
    
    # Signal quality
    directional_accuracy: float
    avg_signal_confidence: float
    confidence_calibration: float  # correlation between confidence and accuracy
    
    # Detailed records
    trades: List[TradeRecord] = field(default_factory=list)
    equity_curve: List[PortfolioSnapshot] = field(default_factory=list)
    
    def to_dict(self) -> Dict:
        return {
            "period": f"{self.start_date.date()} to {self.end_date.date()}",
            "initial_capital": self.initial_capital,
            "final_capital": round(self.final_capital, 2),
            "total_return": f"{self.total_return:.2%}",
            "annualized_return": f"{self.annualized_return:.2%}",
            "sharpe_ratio": round(self.sharpe_ratio, 3),
            "sortino_ratio": round(self.sortino_ratio, 3),
            "max_drawdown": f"{self.max_drawdown:.2%}",
            "total_trades": self.total_trades,
            "win_rate": f"{self.win_rate:.2%}",
            "profit_factor": round(self.profit_factor, 3),
            "directional_accuracy": f"{self.directional_accuracy:.2%}",
            "avg_holding_period": str(self.avg_holding_period),
        }


